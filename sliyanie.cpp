#include <iostream>
#include <vector>
using namespace std;

// Сортировка слиянием (merge sort), схема "разделяй и властвуй".
// Время: O(n log n) — log n уровней рекурсии, на каждом сливается O(n) элементов.
// Память: O(n) дополнительно (временные left/right/merged).
// Возвращает НОВЫЙ отсортированный вектор, исходный a не меняется (он взят по const&).
vector<int> merge_sort(const vector<int>& a) {
    // База рекурсии: массив из 0 или 1 элемента уже отсортирован — возвращаем как есть.
    if (a.size() <= 1) {
        return a;
    }

    // Середина. Каст к int, потому что size() возвращает беззнаковый size_t,
    // а смешивать его со знаковыми переменными — источник предупреждений под -Wall.
    int mid = (int)a.size() / 2;

    // Разбиваем массив на две половины и сортируем их отдельно.
    // left  = элементы [begin, begin+mid)  — первая половина
    // right = элементы [begin+mid, end)    — вторая половина
    vector<int> left(a.begin(), a.begin() + mid);
    vector<int> right(a.begin() + mid, a.end());
    left = merge_sort(left);    // рекурсивно сортируем левую половину
    right = merge_sort(right);  // рекурсивно сортируем правую половину

    // Сливаем две УЖЕ отсортированные половины в один отсортированный массив.
    vector<int> merged;
    merged.reserve(a.size());   // заранее резервируем память под n элементов, чтобы
                                // push_back не перевыделял буфер по ходу дела
    int i = 0;  // указатель по левой половине
    int j = 0;  // указатель по правой половине

    // Пока в обеих половинах ещё есть элементы — сравниваем их "головы"
    // и забираем меньшую. Минимум всего слияния всегда стоит в голове одной
    // из половин, ведь каждая половина уже отсортирована.
    while (i < (int)left.size() && j < (int)right.size()) {
        if (left[i] <= right[j]) {   // <= (а не <) делает сортировку СТАБИЛЬНОЙ:
            merged.push_back(left[i]); // при равенстве берём из левой половины,
            i++;                       // сохраняя исходный порядок равных элементов
        } else {
            merged.push_back(right[j]);
            j++;
        }
    }

    // Одна из половин закончилась. Добавляем "хвост" второй как есть —
    // он уже отсортирован, так что просто дописываем оставшиеся элементы.
    while (i < (int)left.size()) {
        merged.push_back(left[i]);
        i++;
    }
    while (j < (int)right.size()) {
        merged.push_back(right[j]);
        j++;
    }

    return merged;  // возвращаем слитый отсортированный массив на уровень выше
}

int main() {
    int n;
    cin >> n;              // читаем количество элементов
    vector<int> a(n);      // создаём вектор ровно на n элементов
    for (int i = 0; i < n; i++) {
        cin >> a[i];       // читаем сами элементы
    }

    a = merge_sort(a);     // сортируем (функция возвращает новый вектор, присваиваем обратно)

    for (int x : a) {
        cout << x << ' ';  // выводим элементы через пробел
    }
    cout << '\n';          // '\n' вместо endl — не делает лишний flush, чуть быстрее
}